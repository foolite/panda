CREATE OR REPLACE FUNCTION GET_TEST_PRICE(vid IN INTEGER) RETURN NUMBER IS
	vprice NUMBER(10,2);
BEGIN
	SELECT FDECIMAL INTO vprice FROM TEST WHERE ID=vid;
	RETURN(vprice);
END;
/

CREATE OR REPLACE FUNCTION SET_TEST_PRICE(vid IN INTEGER, vprice IN OUT NUMBER) RETURN NUMBER IS 
	vcount INTEGER;
	rprice NUMBER(10,2);
BEGIN
	SELECT COUNT(*) INTO vcount FROM TEST WHERE ID=vid;
	SELECT FDECIMAL INTO rprice FROM TEST WHERE ID=vid;
	UPDATE TEST SET FDECIMAL=vprice WHERE ID=vid;
	vprice := rprice;
	RETURN(vcount);
END;
